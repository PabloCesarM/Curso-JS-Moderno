// operador de > y <

const dinero = 500;
const totalPagar = 300;
//mayor
if(dinero > totalPagar){
    console.log('Si podemos pagar');
}else{
    console.log('Fondo insuficiete');
}
// menor
if(dinero < totalPagar){
    console.log('Fondo insuficiete');
}else{    
    console.log('Si podemos pagar');
}
//mayor o igual
if(dinero >= totalPagar){
    console.log('Si podemos pagar');
}else{
    console.log('Fondo insuficiete');
}
